import Head from 'next/head';
import { useRouter } from 'next/router';
import { GetServerSidePropsContext } from 'next';
import { ArticleList } from '@/components/ArticleList';
import { Header } from '@/components/Header';
import { ArticleInfoResponse } from '@/utils/api';
import { Language } from '@/utils/types';
import {
  ArticleCategory,
  getRandomArticleInfo,
  languageCodes,
} from '@/utils/utils';

type HomeProps = {
  articles: ArticleInfoResponse;
  languages: Language[];
};

export async function getServerSideProps(context: GetServerSidePropsContext) {
  const requestedLanguage = String(context.query.language).toLocaleLowerCase();
  const type = String(
    context.query.type
  ).toLocaleLowerCase() as ArticleCategory;

  if (languageCodes.find((language) => language.code === requestedLanguage)) {
    const articles = await getRandomArticleInfo({
      language: requestedLanguage as never,
      type,
      seenArticleIds: [],
    });
    return {
      props: {
        articles,
      },
    };
  }

  return {
    redirect: {
      destination: '/',
      permanent: false,
    },
  };
}

export default function RandomArticles({ articles }: HomeProps) {
  const { query } = useRouter();
  const language = languageCodes.find(
    (language) => language.code === String(query.language).toLocaleLowerCase()
  );
  if (!language) return null;

  const languageName = language.name;
  const languageCode = language.code;
  const title = `Wikipedia Infinite Scroll - ${languageName}`;

  return (
    <>
      <Head>
        <title>{title}</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Header language={languageName} />
      <ArticleList
        articles={articles}
        articleType={String(query.type)}
        language={languageCode}
      />
    </>
  );
}
